export function getTranslations(lang: Language) {
  return translations[lang] || translations.ru;
}

export function useTranslation() {
  if (typeof window === 'undefined') {
    return { t: translations.ru, lang: 'ru' as Language };
  }
  
  const lang = (localStorage.getItem('language') as Language) || 'ru';
  return { t: translations[lang] || translations.ru, lang };
}

